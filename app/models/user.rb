class User < ActiveRecord::Base
  # Include default devise modules. Others available are:
  # :confirmable, :lockable, :timeoutable and :omniauthable
  devise :database_authenticatable, :registerable, :confirmable,
         :recoverable, :rememberable, :trackable, :validatable
  devise :omniauthable, :omniauth_providers => [:facebook]
  validates :tos, acceptance: true

  has_many :songs
  has_many :liked, :through => :likes, :source => :song
  has_many :likes, -> { order("position ASC") }

  def self.create_from_omniauth(auth, params)
    where(provider: auth.provider, uid: auth.uid).first_or_create do |user|
      user.email = auth.info.email
      user.password = Devise.friendly_token[0,20]
      user.name = auth.info.name   # assuming the user model has a name
      user.musician = params["musician"] == "1"
    end
  end

  def self.get_from_omniauth(auth, params)
    where(provider: auth.provider, uid: auth.uid).first
  end
end
